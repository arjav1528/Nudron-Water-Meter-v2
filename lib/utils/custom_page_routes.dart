import 'package:flutter/material.dart';

/// Creates a slide transition page route for navigating from project selection to dashboard
/// Animation: Right to left (new page slides in from right)
class SlideRightToLeftRoute<T> extends PageRouteBuilder<T> {
  final Widget page;

  SlideRightToLeftRoute({required this.page})
      : super(
          pageBuilder: (context, animation, secondaryAnimation) => page,
          transitionDuration: const Duration(milliseconds: 300),
          reverseTransitionDuration: const Duration(milliseconds: 300),
          transitionsBuilder: (context, animation, secondaryAnimation, child) {
            const begin = Offset(1.0, 0.0);
            const end = Offset.zero;
            const curve = Curves.easeInOut;

            var tween = Tween(begin: begin, end: end).chain(
              CurveTween(curve: curve),
            );

            return SlideTransition(
              position: animation.drive(tween),
              child: child,
            );
          },
        );
}

